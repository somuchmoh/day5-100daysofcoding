fruits = ["Apple", "Peach", "Pear"]
for fruit in fruits:
  print(fruit)
#It loops through the list of fruits and prints it out for every list item "fruit".
  print(fruit + " Pie")
#We can do many things inside the for loop 

#Using for loop with range function
for number in range(1,10):   #1 to 9 (10 not included)
  print(number)

for number1 in range(1,10,2):  #prints every second number
  print(number1)

